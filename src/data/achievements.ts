export type AchievementTier = 'bronze' | 'silver' | 'gold' | 'platinum' | 'diamond';
export type AchievementSeries =
  | 'scholar'      // 学者系列
  | 'warrior'      // 战士系列
  | 'guardian'     // 守护者系列
  | 'explorer'     // 探索者系列
  | 'master'       // 大师系列
  | 'legendary'    // 传奇系列
  | 'merchant'     // 商人系列（新）
  | 'timemaster'   // 时间大师系列（新）
  | 'collector'    // 收藏家系列（新）
  | 'perfectionist' // 完美主义系列（新）
  | 'digitartist'  // 数字艺术家系列（新）
  | 'extreme'      // 极限挑战系列（新）
  | 'habit'        // 习惯养成系列（新）
  | 'event'        // 特殊事件系列（新）
  | 'speed'        // 速度与激情系列（新）
  | 'none';

export interface AchievementData {
  id: string;
  title: string;
  description: string;
  category: 'milestone' | 'streak' | 'attribute' | 'quest' | 'special' | 'hidden' | 'collection' | 'time' | 'numeric' | 'event';
  tier: AchievementTier;
  icon: string;
  requirement: number;
  reward: {
    exp: number;
    coins: number;
    title?: string;
    points?: number;  // 成就点数
  };
  hidden?: boolean;
  // 复杂特性
  series?: AchievementSeries;  // 所属系列
  prerequisites?: string[];    // 前置成就ID列表
  isProgression?: boolean;     // 是否为进阶成就
  progressionLevel?: number;   // 进阶等级（1-5）
  nextInChain?: string;        // 链式成就的下一个ID
  rarity?: 'common' | 'rare' | 'epic' | 'legendary';  // 稀有度
  timeLimit?: number;          // 时间限制（小时）
  comboRequirement?: {         // 组合要求
    achievements?: string[];   // 需要同时拥有的成就
    attributes?: { [key: string]: number };  // 需要同时达到的属性
  };
}

